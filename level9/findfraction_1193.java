public class findfraction_1193 {
    public static void main(String[] args) {
        for (int i = 1; i < 11; i++) {
            for (int j = (i*10)+1; j < ((i+1)*10); j++) {
                System.out.print(j+" ");
            }
            System.out.println("");
        }
    }
}
